	public static ItemSeeds itemSetting(BlockCrop block, String Mod, String name, String tier){
		if(Mods.checkDevEnv()){
			return new ItemSeeds(block,
			new FabricItemSettings().group(Main.GROUP),
			tier,
			Mod,
			name
		);
		}else{
			return new ItemSeeds(block,
			new FabricItemSettings().group(Main.GROUP_SEEDS),
			tier,
			Mod,
			name
		);
		}
	}

